					<div id="contenu_poeme">
						<p id="Strophe_0" class='strophe'>
							En ce temps-là,<br>
							En ce Temps-ci donc,<br>
							Le monde était encore...<br>
							A la fois le même ; en apparence.<br>
							A la fois Autre ; dans son essence.<br>
						</p>
						
						<p id="Strophe_1" class='strophe'>
						Qui peut reconnaître, au milieu de l’hiver,<br>
						La branche morte, de l’endormie ?<br>
						Rien ne les distingue,<br>
						Si ce n’est… dans l’au-profond de l’écorce,<br>
						Le torrent d’Incandescence...<br>
						</p>
						
						<p id="Strophe_2" class='strophe'>
						En ce temps-là. En ce Temps-ci donc,<br>
						Ton monde<br>
						Est dans l’endormi de l’hiver :<br>
						Froid en apparence,<br>
						Bouillonnant dans son profond.<br>
						</p>
						
						<p id="Strophe_3" class='strophe'>
						A la nuit des temps,<br>
						Le Maître des Saisons vous a choisis,<br>
						Plantés, émondés, raffermis.<br>
						</p>
						
						<p id="Strophe_4" class='strophe'>
						Il vous a offert en greffe<br>
						Jusqu’à l’Âme de Son Âme<br>
						Pour que coule en vous Sa Sève…<br>
						</p>
						
						<p id="Strophe_5" class='strophe'>
						Et puis…<br>
						</p>
						
						<p id="Strophe_6" class='strophe'>
						L’heure est venue,<br>
						Petite Fleur endormie,<br>
						-Oui, Toi qui me lis-<br>
						</p>
						
						<p id="Strophe_7" class='strophe'>
						L’heure est venue <br>
						De Te réveiller de Ma Tendresse <br>
						Pour que s’accomplisse Ta Promesse.<br>
						</p>
						
						<p id="Strophe_8" class='strophe'>
						Penché sur le galbe de ton bourgeon, <br>
						Voici. Je T’appelle par Ton Nom…<br>
						</p>
						
						<p id="Strophe_9" class='strophe'>
						En ce temps-là, <br>
						En ce Temps-ci, donc <br>
						Voici qu’il T’est donné<br>
						</p>
						
						<p id="Strophe_10" class='strophe lastStrophe'>
						De Vivre Qui Tu Es.<br>
